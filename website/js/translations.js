// i18n Engine
const i18n = {
  currentLang: 'en',
  rtlLangs: ['ar'],

  init() {
    const saved = localStorage.getItem('voipat_lang');
    const browserLang = navigator.language?.substring(0, 2);
    this.currentLang = saved || (translations['lang.' + browserLang] ? browserLang : 'en');
    this.apply();
    this.buildSwitcher();
  },

  apply() {
    const lang = this.currentLang;
    localStorage.setItem('voipat_lang', lang);

    // Set RTL
    if (this.rtlLangs.includes(lang)) {
      document.documentElement.setAttribute('dir', 'rtl');
      document.documentElement.setAttribute('lang', lang);
      document.body.classList.add('rtl');
    } else {
      document.documentElement.setAttribute('dir', 'ltr');
      document.documentElement.setAttribute('lang', lang);
      document.body.classList.remove('rtl');
    }

    // Translate all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const t = translations[key];
      if (t && t[lang]) {
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
          el.placeholder = t[lang];
        } else {
          el.textContent = t[lang];
        }
      }
    });

    // Translate data-i18n-html (for elements with inner HTML like icons)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      const t = translations[key];
      if (t && t[lang]) {
        // Preserve leading icon if present
        const icon = el.querySelector('i');
        if (icon) {
          el.innerHTML = icon.outerHTML + ' ' + t[lang];
        } else {
          el.textContent = t[lang];
        }
      }
    });

    // Update active item in language bar
    document.querySelectorAll('.lang-item').forEach(item => {
      item.classList.toggle('active', item.dataset.lang === lang);
    });
  },

  buildSwitcher() {
    // Bind click events to the language bar items
    document.querySelectorAll('.lang-item').forEach(item => {
      item.addEventListener('click', (e) => {
        e.preventDefault();
        this.currentLang = item.dataset.lang;
        this.apply();
      });
    });
  },

  setLang(code) {
    this.currentLang = code;
    this.apply();
  }
};

// Initialize when DOM is ready
document.addEventListener('DOMContentLoaded', () => i18n.init());
